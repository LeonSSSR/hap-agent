type CatalogOperation = PlatformOperation & {
  parent_ui_action_id?: string;
  action_type?: string;
};

function defaultSelector(uiActionId: string, actionType: string): string {
  if (actionType === 'fill') {
    return `[data-agent-action-id="${uiActionId}"] input, input[data-agent-action-id="${uiActionId}"], [data-agent-action-id="${uiActionId}"]`;
  }
  return `[data-agent-action-id="${uiActionId}"], [data-agent-page-root="${uiActionId}"]`;
}

function buildCatalogDerivedActions(): Record<string, AgentActionDefinition> {
  const actions: Record<string, AgentActionDefinition> = {};
  const operations = (catalog.operations || []) as CatalogOperation[];
  for (const op of operations) {
    const uiActionId = op.ui_action_id;
    if (!uiActionId || uiActionId.endsWith('.open')) continue;
    const actionType = String(op.action_type || 'navigate').toLowerCase();
    if (actionType === 'navigate') continue;
    if (BUTTON_ACTION_OVERRIDES[uiActionId] || WORKFLOW_AND_LINEAGE_ACTIONS[uiActionId]) continue;
    actions[uiActionId] = {
      uiActionId,
      type: actionType as AgentPageActionType,
      selector: defaultSelector(uiActionId, actionType),
      route: op.route,
      label: op.label,
      description: `执行「${op.label}」页面操作（${actionType}）。`,
    };
  }
  return actions;
}

let registryCache: Record<string, AgentActionDefinition> | null = null;

function buildRegistry(): Record<string, AgentActionDefinition> {
  return {
    ...buildPlatformAgentActions(),
    ...WORKFLOW_AND_LINEAGE_ACTIONS,
    ...BUTTON_ACTION_OVERRIDES,
    ...buildCatalogDerivedActions(),
  };
}

export function getAgentActionRegistry(): Record<string, AgentActionDefinition> {
  if (!registryCache) {
    registryCache = buildRegistry();
  }
  return registryCache;
}

export function getAgentActionDefinition(uiActionId?: string | null): AgentActionDefinition | undefined {
  if (!uiActionId) return undefined;
  return getAgentActionRegistry()[uiActionId];
}

export function listRegisteredUiActionIds(): string[] {
  return Object.keys(getAgentActionRegistry()).sort();
}

/** 供测试或热更新 catalog 后刷新注册表 */
export function resetAgentActionRegistryCache(): void {
  registryCache = null;
}
